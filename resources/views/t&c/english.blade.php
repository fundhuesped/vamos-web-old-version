<h4>Thank you for using VAMOS.</h4>

<br/>

<p>
	VAMOS allows you to search and locate places that offer sexual and reproductive health services on a map. You can also find information about the services offered at each location: type of service, address, telephone number, e-mail, website, and other information about IPPF/WHR Member Associations, associated clinics, partner organizations and sevices form the public sector.
</p>

<p>
	Platform users can suggest the inclusion of new locations. Applications will be reviewed by IPPF/WHR and Fundación Huésped.
</p>

<p>
	In VAMOS, users can rate the care received at the location they visited through an anonymous survey. IPPF/WHR and Fundación Huésped are not responsible for the comments written by users. IPPF/WHR and Fundación Huésped have the authority to delete comments that violate local legislation, reveal private or delicate information, contain advertisements or infringe human rights.
</p>

<p>
	Contact details collected in the surveys will be used by IPPF/WHR and Fundación Huésped to gather information about the platform and give feedback to the Association offering the services. Users may request to be removed from the distribution list at any time by emailing VAMOS at vamoslacapp@gmail.com.
</p>

<p>
	VAMOS offers online counselling services where users can ask their questions about sexual and reproductive health. A virtual assistant (chatbot) answers the questions 24/7. If the intervention of a person is required, they are online on working days in Argentina form Monday to Friday, from 12pm to 8pm (Buenos Aires GMT-3). Outside these times, questions can be submitted through a contact form.
</p>

<p>
	Contact details and questions sent by users of the online counseling service will be treated confidentially and will be used by IPPF/WHR and Fundación Huésped to provide the requested information. Besides, users will be offered to join to the distribution list, under the same conditions mentioned above.
</p>

<p>
	The VAMOS app for Android OS has the Facebook pixel code installed. This is used to create custom audiences for ads and measure conversion rates. After May 25, 2018, we must first obtain your consent as a user before Facebook can track your activity. Therefore, if you want to download the app version, you will first have to accept the terms and conditions.
</p>

<p>
	Users may share information found in VAMOS, disseminate the platform by different means, use it for personal, educational, work or other not-for-profit purposes. Please make sure to always mention VAMOS, as well as to inform us about your user experiences.
</p>

<p>
	VAMOS does not have any commercial purposes and its use is totally free. No person or institution may use the platform or its functionalities for profit.
</p>

<p>
	The actual conditions may differ from the results obtained through the platform. We invite users to participate in order to improve the accuracy of the information offered. VAMOS aims to be a source of information for users, who are responsible for the decisions made based on the information provided on the platform.
</p>

