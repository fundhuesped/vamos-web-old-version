<h4>Gracias por utilizar VAMOS.</h4>

<br/>

<p>
	VAMOS permite buscar y ver en un mapa, lugares que ofrecen diferentes servicios de salud sexual y reproductiva. De cada lugar, se puede encontrar información sobre los servicios que ofrece: tipo de servicio, dirección, teléfono, correo electrónico, sitio web, entre otros datos de las Asociaciones Miembro de IPPF/RHO, sus clínicas asociadas, organizaciones aliadas y servicios del sector público.
</p>

<p>
	Las y los usuarios de la plataforma pueden sugerir la incorporación de nuevos sitios. Las solicitudes serán evaluadas por IPPF/RHO y Fundación Huésped para su incorporación.
</p>

<p>
	En VAMOS, las y los usuarios pueden evaluar la atención recibida en los servicios localizados, mediante una encuesta anónima. IPPF/RHO y Fundación Huésped no se responsabilizan por las evaluaciones y comentarios hechos por los usuarios. IPPF/RHO y Fundación Huésped tienen la potestad de eliminar comentarios cuando violen normas de cada país, revelen información privada o sensible, contengan publicidad o atenten contra los derechos humanos.
</p>

<p>
	Los datos de contacto solicitados en la encuesta de evaluación, serán utilizados por IPPF/RHO y Fundación Huésped para brindar más información sobre la plataforma y proporcionar retroalimentación a las Asociaciones que prestan los servicios. Las y los usuarios podrán solicitar la baja de la lista de distribución cuando lo deseen comunicándose a vamoslacapp@gmail.com.
</p>

<p>
	VAMOS ofrece un servicio de consejería online en el que las y los usuarios pueden hacer consultas sobre salud sexual y reproductiva. Un asistente virtual (chatbot) responde a las consultas las 24 horas todos los días del año. En el caso de requerirse la intervención de una persona del equipo de atención, las mismas están disponibles de lunes a viernes en días hábiles en Argentina, entre las 12 y 20 hs. (Buenos Aires GMT-3). Fuera de ese horario es posible enviar la consulta mediante un formulario de contacto.
</p>

<p>
	Los datos de contacto enviados por las personas en el marco del servicio de consejería online, serán utilizados por IPPF/RHO y Fundación Huésped para brindar la información solicitada. Además se les ofrecerá incorporarse a la lista de distribución, bajo las mismas condiciones mencionadas anteriormente.
</p>

<p>
	La aplicación de VAMOS para Android OS tiene instalada el píxel de Facebook, el cual es usado para crear públicos personalizados para anuncios y medir tasa de conversión. A partir del 25 de mayo de 2018, debemos obtener su consentimiento como usuario antes que Facebook pueda hacer seguimiento de su actividad. Por lo tanto, si quiere descargar la aplicación, primero tendrá que aceptar los términos y condiciones.
</p>

<p>
	Las y los usuarios pueden compartir la información encontrada en VAMOS, difundir la plataforma por diferentes medios, usarla con fines no lucrativos personales, educativos, laborales u otros. Se solicita mencionar siempre a VAMOS, así como informar sobre experiencias de uso.
</p>

<p>
	VAMOS no tiene fines comerciales y el uso es totalmente gratuito, ninguna persona o institución puede usar la plataforma o sus funcionalidades con fines de lucro.
</p>

<p>
	Las condiciones reales pueden diferir de los resultados obtenidos mediante la plataforma, apelamos a la participación de las y los usuarios para la corrección y mejora de la información ofrecida. VAMOS busca ser una fuente de referencia para las personas, quienes en última instancia son responsables de las decisiones tomadas con la información aquí ofrecida.
</p>

